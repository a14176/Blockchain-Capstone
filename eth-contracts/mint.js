async function main() {
    const network = NETWORK === "mainnet" || NETWORK === "live" ? "mainnet" : "rinkeby";
    console.log("MNEMONIC " + MNEMONIC);

    const provider = new HDWalletProvider({
        mnemonic: MNEMONIC,
        providerOrUrl: "https://" + network + ".infura.io/v3/" + NODE_API_KEY
    });
    const web3Instance = new web3(provider);

    if (NFT_CONTRACT_ADDRESS) {
        const nftContract = new web3Instance.eth.Contract(
            NFT_ABI,
            NFT_CONTRACT_ADDRESS,
            { gasLimit: "1000000" }
        );

        // Creatures issued directly to the owner.
        for (var i = 0; i < NUM_CREATURES; i++) {
            let x = 10+i;
            let proofFile = 'proof-'+x+'.json';
            console.log("proofFile " + proofFile);
            let rawdata = fs.readFileSync('../zokrates/code/square/proofs/' + proofFile);
            let Proof = JSON.parse(rawdata);
            console.log("Input " + Proof.inputs);
            const result = await nftContract.methods
                .mintNFT(OWNER_ADDRESS, i + 1, Proof.proof, Proof.inputs)
                .send({ from: OWNER_ADDRESS });
            console.log("Minted NFT. Transaction: " + result.transactionHash);
        }
    } else {
        console.error(
            "Add NFT_CONTRACT_ADDRESS to the environment variables"
        );
    }
    process.exit();
}

main();